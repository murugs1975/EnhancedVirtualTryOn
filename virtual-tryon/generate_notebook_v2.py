"## 5. Stage 1 — GMM Training (Cloth Warping) <a id='5'></a>\n\n"
"**Loss**: L1(warped_mask, cloth_region) + 0.01 × L2(theta)\n\n"
"L1 alignment forces the warped cloth mask to cover the person's clothing region.\n"
"L2 on theta regularises the TPS offsets, preventing degenerate warps."
