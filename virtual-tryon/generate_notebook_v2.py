"class VGGPerceptualLoss(nn.Module):\n"
"    # Feature layers: relu1_2 (w=1.0), relu2_2 (w=0.75), relu3_3 (w=0.5)\n"
"    # Input: images in [-1,1]; converts to VGG-normalised [0,1] internally\n\n"
