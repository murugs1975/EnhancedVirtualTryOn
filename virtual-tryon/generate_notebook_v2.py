# ── 12 · TPS grid generator ───────────────────────────────────────────────────
C.append(md("### 4.1 Thin Plate Spline (TPS) Grid Generator\n"
            "Implements the TPS basis U(r) = r² log(r²) to compute a dense backward-warp grid from predicted control-point offsets."))
C.append(code(
"class TPSWarp(nn.Module):\n"
"    # Thin Plate Spline warping.\n"
"    # theta : (B, 2, N) offsets for NxN control points\n"
"    # returns: sampling grid (B, H, W, 2) for F.grid_sample\n\n"
"    def __init__(self, img_h=256, img_w=192, n_ctrl=5):\n"
"        super().__init__()\n"
"        self.img_h=img_h; self.img_w=img_w\n"
"        self.N = n_ctrl*n_ctrl\n\n"
"        cx = torch.linspace(-1,1,n_ctrl)\n"
"        cy = torch.linspace(-1,1,n_ctrl)\n"
"        gy,gx = torch.meshgrid(cy,cx,indexing='ij')\n"
"        ctrl  = torch.stack([gx.flatten(),gy.flatten()],dim=1)  # (N,2)\n"
"        self.register_buffer('ctrl_src', ctrl)\n\n"
"        ox = torch.linspace(-1,1,img_w)\n"
"        oy = torch.linspace(-1,1,img_h)\n"
"        ogy,ogx = torch.meshgrid(oy,ox,indexing='ij')\n"
"        out_pts = torch.stack([ogx.flatten(),ogy.flatten()],dim=1)  # (H*W,2)\n"
"        self.register_buffer('out_pts', out_pts)\n\n"
"    @staticmethod\n"
"    def _U(r2):\n"
"        r2 = r2.clamp(min=1e-10)\n"
"        return r2 * torch.log(r2)\n\n"
"    @staticmethod\n"
"    def _K(a, b):\n"
"        diff = a.unsqueeze(1)-b.unsqueeze(0)   # (n,m,2)\n"
"        return TPSWarp._U((diff**2).sum(-1))   # (n,m)\n\n"
"    def forward(self, theta):\n"
"        B,_,N = theta.shape\n"
"        dev   = theta.device\n"
"        ctrl_src = self.ctrl_src.to(dev)\n"
"        ctrl_tgt = ctrl_src.unsqueeze(0) + theta.permute(0,2,1)  # (B,N,2)\n"
"        out_pts  = self.out_pts.to(dev)\n"
"        HW = out_pts.shape[0]\n\n"
"        grids = []\n"
"        for b in range(B):\n"
"            C_t = ctrl_tgt[b]          # (N,2) target positions\n"
"            C_s = ctrl_src             # (N,2) source values\n"
"            K   = self._K(C_t,C_t) + 1e-6*torch.eye(N,device=dev)\n"
"            ones = torch.ones(N,1,device=dev)\n"
"            P    = torch.cat([ones,C_t],dim=1)   # (N,3)\n"
"            L    = torch.cat([\n"
"                torch.cat([K,P],dim=1),\n"
"                torch.cat([P.t(),torch.zeros(3,3,device=dev)],dim=1)\n"
"            ],dim=0)                             # (N+3,N+3)\n"
"            try:\n"
"                wx = torch.linalg.solve(L, torch.cat([C_s[:,0],torch.zeros(3,device=dev)]))\n"
"                wy = torch.linalg.solve(L, torch.cat([C_s[:,1],torch.zeros(3,device=dev)]))\n"
"            except Exception:\n"
"                grids.append(torch.stack([out_pts[:,0],out_pts[:,1]],dim=-1))\n"
"                continue\n"
"            Phi   = self._K(out_pts,C_t)\n"
"            P_out = torch.cat([torch.ones(HW,1,device=dev),out_pts],dim=1)  # (HW,3)\n"
"            basis = torch.cat([Phi,P_out],dim=1)  # (HW,N+3)\n"
"            grids.append(torch.stack([basis@wx, basis@wy],dim=-1))\n\n"
"        grid = torch.stack(grids,dim=0).reshape(B,self.img_h,self.img_w,2)\n"
"        return grid.clamp(-1,1)\n\n\n"
"# Unit test\n"
"_tps   = TPSWarp(IMG_H, IMG_W, N_CTRL)\n"
"_theta = torch.zeros(2, 2, N_CTRL**2)\n"
"_grid  = _tps(_theta)\n"
"_warp  = F.grid_sample(torch.randn(2,3,IMG_H,IMG_W),_grid,align_corners=True)\n"
"print(f'TPS unit test OK -> grid {tuple(_grid.shape)}, warped {tuple(_warp.shape)}')\n"
))

# ── 13 · GMM ──────────────────────────────────────────────────────────────────
C.append(md("### 4.2 Geometric Matching Module (GMM)\n"
            "CNN feature extraction + cross-correlation + TPS regression."))
C.append(code(
"class FeatureExtractor(nn.Module):\n"
"    # 3-layer CNN -> (B, 64, H/8, W/8) feature maps\n"
"    def __init__(self, in_ch=3, out_ch=64):\n"
"        super().__init__()\n"
"        self.net = nn.Sequential(\n"
"            nn.Conv2d(in_ch,32,3,padding=1,bias=False), nn.BatchNorm2d(32), nn.ReLU(True), nn.MaxPool2d(2),\n"
"            nn.Conv2d(32,64,3,padding=1,bias=False),    nn.BatchNorm2d(64), nn.ReLU(True), nn.MaxPool2d(2),\n"
"            nn.Conv2d(64,out_ch,3,padding=1,bias=False),nn.BatchNorm2d(out_ch),nn.ReLU(True),nn.MaxPool2d(2),\n"
"        )\n"
"    def forward(self,x): return self.net(x)\n\n\n"
"class CorrelationLayer(nn.Module):\n"
"    # Cross-correlation: (B,C,H,W)x2 -> (B, H*W, H, W)\n"
"    # Each output channel = dot product with one reference feature vector\n"
"    def forward(self,fa,fb):\n"
"        B,C,H,W = fa.shape\n"
"        fa_n = F.normalize(fa.view(B,C,-1),dim=1)  # (B,C,HW)\n"
"        fb_n = F.normalize(fb.view(B,C,-1),dim=1)\n"
"        corr = torch.bmm(fa_n.permute(0,2,1),fb_n)  # (B,HW,HW)\n"
"        return corr.view(B,H*W,H,W)\n\n\n"
"class GMM(nn.Module):\n"
"    # Geometric Matching Module (CP-VTON style):\n"
"    #   agnostic + cloth -> FeatureExtractor -> CorrelationLayer\n"
"    #   -> regressor -> theta (TPS offsets) -> TPSWarp -> warped cloth/mask\n\n"
"    def __init__(self, n_ctrl=5, img_h=256, img_w=192):\n"
"        super().__init__()\n"
"        self.N = n_ctrl*n_ctrl\n"
"        fh,fw = img_h//8, img_w//8  # feature map size: 32x24\n"
"        corr_ch = fh*fw             # 768 correlation channels\n\n"
"        self.feat_p = FeatureExtractor(3, 64)\n"
"        self.feat_c = FeatureExtractor(3, 64)\n"
"        self.corr   = CorrelationLayer()\n\n"
"        self.reg = nn.Sequential(\n"
"            nn.Conv2d(corr_ch,128,3,padding=1), nn.ReLU(True), nn.MaxPool2d(2),\n"
"            nn.Conv2d(128,64,3,padding=1),      nn.ReLU(True), nn.AdaptiveAvgPool2d((4,4)),\n"
"            nn.Flatten(),\n"
"            nn.Linear(64*16,256), nn.ReLU(True), nn.Dropout(0.5),\n"
"            nn.Linear(256, 2*self.N), nn.Tanh(),\n"
"        )\n"
"        self.tps   = TPSWarp(img_h, img_w, n_ctrl)\n"
"        self.scale = 0.4\n\n"
"    def forward(self, agnostic, cloth, cloth_mask):\n"
"        corr  = self.corr(self.feat_p(agnostic), self.feat_c(cloth))\n"
"        theta = (self.reg(corr)*self.scale).view(-1,2,self.N)\n"
"        grid         = self.tps(theta)\n"
"        warped_cloth = F.grid_sample(cloth,      grid, mode='bilinear', padding_mode='border', align_corners=True)\n"
"        warped_mask  = F.grid_sample(cloth_mask, grid, mode='bilinear', padding_mode='zeros',  align_corners=True)\n"
"        return warped_cloth, warped_mask, theta\n\n\n"
"gmm   = GMM(n_ctrl=N_CTRL, img_h=IMG_H, img_w=IMG_W).to(DEVICE)\n"
"p_gmm = sum(p.numel() for p in gmm.parameters() if p.requires_grad)\n"
"print(f'GMM parameters: {p_gmm:,}')\n"
"_b  = next(iter(train_dl))\n"
"_wc,_wm,_th = gmm(_b['agnostic'],_b['cloth'],_b['cloth_mask'])\n"
"print(f'GMM -> warped_cloth {tuple(_wc.shape)}, warped_mask {tuple(_wm.shape)}, theta {tuple(_th.shape)}')\n"
))

# ── 14 · TOM ──────────────────────────────────────────────────────────────────
C.append(md("### 4.3 Try-On Module (TOM)\nU-Net with dual output head: rendered image + soft composition mask."))
C.append(code(
"class ConvBlock(nn.Module):\n"
"    def __init__(self,ic,oc,drop=0.1):\n"
"        super().__init__()\n"
"        self.b = nn.Sequential(\n"
"            nn.Conv2d(ic,oc,3,padding=1,bias=False),nn.BatchNorm2d(oc),nn.ReLU(True),\n"
"            nn.Conv2d(oc,oc,3,padding=1,bias=False),nn.BatchNorm2d(oc),nn.ReLU(True),\n"
"            nn.Dropout2d(drop),\n"
"        )\n"
"    def forward(self,x): return self.b(x)\n\n\n"
"class TOM(nn.Module):\n"
"    # U-Net: 7ch [agnostic|warped_cloth|warped_mask] -> rendered(3ch) + mask(1ch)\n"
"    # Final: output = mask * warped_cloth + (1-mask) * rendered\n\n"
"    def __init__(self, in_ch=7, features=(32,64,128)):\n"
"        super().__init__()\n"
"        f = list(features)\n"
"        self.pool = nn.MaxPool2d(2)\n"
"        self.e1=ConvBlock(in_ch,f[0],0.1); self.e2=ConvBlock(f[0],f[1],0.2)\n"
"        self.e3=ConvBlock(f[1],f[2],0.3); self.bt=ConvBlock(f[2],f[2]*2,0.3)\n"
"        self.u3=nn.ConvTranspose2d(f[2]*2,f[2],2,2); self.d3=ConvBlock(f[2]*2,f[2],0.2)\n"
"        self.u2=nn.ConvTranspose2d(f[2],f[1],2,2);   self.d2=ConvBlock(f[1]*2,f[1],0.1)\n"
"        self.u1=nn.ConvTranspose2d(f[1],f[0],2,2);   self.d1=ConvBlock(f[0]*2,f[0])\n"
"        self.rh = nn.Sequential(nn.Conv2d(f[0],3,1),nn.Tanh())\n"
"        self.mh = nn.Sequential(nn.Conv2d(f[0],1,1),nn.Sigmoid())\n"
"        self._init()\n\n"
"    def _init(self):\n"
"        for m in self.modules():\n"
"            if isinstance(m,nn.Conv2d): nn.init.kaiming_normal_(m.weight,nonlinearity='relu')\n"
"            elif isinstance(m,nn.BatchNorm2d): nn.init.constant_(m.weight,1); nn.init.constant_(m.bias,0)\n\n"
"    def forward(self,x,warped_cloth):\n"
"        e1=self.e1(x); e2=self.e2(self.pool(e1)); e3=self.e3(self.pool(e2))\n"
"        bt=self.bt(self.pool(e3))\n"
"        d3=self.d3(torch.cat([self.u3(bt),e3],1))\n"
"        d2=self.d2(torch.cat([self.u2(d3),e2],1))\n"
"        d1=self.d1(torch.cat([self.u1(d2),e1],1))\n"
"        rendered=self.rh(d1); mask=self.mh(d1)\n"
"        return mask*warped_cloth+(1-mask)*rendered, rendered, mask\n\n\n"
"tom   = TOM(in_ch=7).to(DEVICE)\n"
"p_tom = sum(p.numel() for p in tom.parameters() if p.requires_grad)\n"
"print(f'TOM parameters: {p_tom:,}')\n"
"_xi = torch.cat([_b['agnostic'],_wc.detach(),_wm.detach()],dim=1)\n"
"_out,_ren,_msk = tom(_xi,_wc.detach())\n"
"print(f'TOM -> output {tuple(_out.shape)}, mask {tuple(_msk.shape)}')\n"
))

